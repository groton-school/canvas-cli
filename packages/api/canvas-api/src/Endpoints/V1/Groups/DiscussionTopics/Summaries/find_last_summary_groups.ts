import { client, Masquerade } from '#client';
import { JSONValue } from '@battis/typescript-tricks';

export type find_last_summary_groupsPathParameters = {
  /**
   * ID
   *
   * Type: string
   */
  group_id: string | number;
  /**
   * ID
   *
   * Type: string
   */
  topic_id: string | number;
};

export type find_last_summary_groupsSearchParameters = Masquerade;

type Options = (
  | {
      path: find_last_summary_groupsPathParameters;
    }
  | {
      /** @deprecated Use {@link Options.path} */
      pathParams: find_last_summary_groupsPathParameters;
    }
) &
  (
    | {
        query?: Partial<find_last_summary_groupsSearchParameters>;
        /** @deprecated Use {Options.query} */
        searchParams?: Partial<find_last_summary_groupsSearchParameters>;
        strict?: false;
      }
    | ((
        | {
            query: find_last_summary_groupsSearchParameters;
          }
        | {
            /** @deprecated Use {Options.query} */
            searchParams: find_last_summary_groupsSearchParameters;
          }
      ) & {
        strict: true;
      })
  );

/**
 * Find Last Summary
 *
 * Returns: (1) last userInput (what current user had keyed in to produce the
 * last discussion summary), (2) last discussion summary generated by the
 * current user for current discussion topic, based on userInput, (3) and some
 * usage information.
 *
 * Nickname: find_last_summary_groups
 */
export async function find_last_summary_groups(options: Options) {
  const response = await client().fetchAs<JSONValue>(
    `/api/v1/groups/{group_id}/discussion_topics/{topic_id}/summaries`,
    {
      method: 'GET',
      ...options
    }
  );
  return response;
}
